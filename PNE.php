    <p>
      Parokya ni Edgar is a Filipino band that was
      formed in 1993 by a group of college students.
      The band is famous and most lauded for its original
      rock novelty songs and often satirical covers of popular songs.
      The band has since transcended musical genres, varying styles
      from one song to another alternative rock to pop rock, funk
      to rapcore, and so on  while providing comic relief to their
      listeners. Despite having “Edgar” in the band’s name, none of
      the members go by it.
    </p>
    <p>
      Naming themselves Comic Relief, the band originally consisted of
      three vocalists Chito Miranda, Jeric Estaco, and Vinci Montaner,
      and two guitarists Miko Yap and Gab Chee Kee. They regularly played
      in after school jam sessions before performing an opening number for
      an Eraserheads concert. This served as their break into the local music
      scene and prompted them to add a drummer and a bassist Dindin Moreno and
      Buwi Meneses, respectively. The same performance also marked the change of
      the band’s name to Parokya ni Edgar. After high school, Miko and Jeric
      decided to withdraw from the band to pursue other interests. As a replacement,
      the remaining band members invited their friend guitarist Darius Semaña (lead guitar)
      to their fold.
    </p>
